# Python Math

# Built-in Math Functions

# Example: Finding the lowest and highest value
x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

# Example: Finding the absolute value
x = abs(-7.25)
print(x)

# Example: Calculating the power of a number
x = pow(4, 3)
print(x)

# The Math Module

# To access more advanced math functions, import the math module.

import math

# Example: Finding the square root of a number
x = math.sqrt(64)
print(x)

# Example: Rounding a number upwards and downwards
x = math.ceil(1.4)
y = math.floor(1.4)
print(x)  # returns 2
print(y)  # returns 1

# Example: Accessing the value of pi
x = math.pi
print(x)
